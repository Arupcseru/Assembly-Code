.MODEL SMALL
.STACK 100H
.DATA
	X DB 30 DUP ('\n')

.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS, AX
	MOV SI,0
	MOV AH,1
NEXT:
	INT 21H
	CMP AL,0DH
	JZ EXIT
	
	MOV X[SI], AL
	INC SI
	JMP NEXT
EXIT:
	DEC SI
;print a new line

	MOV AH, 2
	MOV DL, 0AH
	INT 21H
	MOV DL, 0DH
	INT 21H
;Output
	;MOV DI, 0
	MOV AH,2
NEXT2:
	MOV DL, X[SI]
	INT 21H
	DEC SI
	CMP SI,-1
	JG NEXT2
	
	MOV AH,4CH
	MAIN ENDP
	END MAIN
	
	
	
	